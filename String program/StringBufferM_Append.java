class  StringBufferM_Append
{
	public static void main(String[] args) {
		StringBuffer sb= new StringBuffer();
		sb.append("Karthik"+"\n");
		sb.append("has"+"\n");
		sb.append(2+"Girl Friends"+"\n");
		sb.append("and it is true");

		System.out.println(sb);

	}
}

